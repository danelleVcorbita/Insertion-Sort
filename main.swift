// Add your code below:
var array = unsortedIntegers
var x = 0
var swap = 0
var total = 0
for num in 0..<array.count {
    var n = num
    while n > 0 && array[n] < array[n-1] {
        x = array[n]
        array[n] = array[n-1]
        array[n-1] = x
        swap += 1
        total += 1
        n -= 1
    }
    print("Pass: \(num), Swaps: \(swap)/\(total), Array: \(array)")
    swap = 0
}
